
#Loops

#for
for i in range(5):
    print(i)

#while
i = 1
while i <= 5:
    print(i)
    i += 1
    
#range
for i in range(1, 10, 2):
    print(i)

#break
for i in range(10):
    if i == 5:
        break
    print(i)
    
#continue

for i in range(1, 6):
    if i == 3:
        continue
    print(i)
